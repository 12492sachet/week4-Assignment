// This function calculates the area of a rectangle
function calculateArea(length, width) {
  let area = length * width; // Multiply the length and width to get the area
  return area; // Return the calculated area
}

let length = 5; // Length of the rectangle
let width = 8; // Width of the rectangle
let area = calculateArea(length, width); // Calling the function to calculate the area

// Displaying the calculated area to the console
console.log("The area of the rectangle is: " + area);
